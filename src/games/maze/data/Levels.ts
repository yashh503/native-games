export const findStartPosition = (grid: number[][]): { row: number; col: number } => {
  for (let row = 0; row < grid.length; row++) {
    for (let col = 0; col < grid[row].length; col++) {
      if (grid[row][col] === 2) {
        return { row, col };
      }
    }
  }
  return { row: 0, col: 0 };
};

export const findExitPosition = (grid: number[][]): { row: number; col: number } => {
  for (let row = 0; row < grid.length; row++) {
    for (let col = 0; col < grid[row].length; col++) {
      if (grid[row][col] === 3) {
        return { row, col };
      }
    }
  }
  return { row: grid.length - 1, col: grid[0].length - 1 };
};
